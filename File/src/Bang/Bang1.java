    public void setSelectRow(int i, JTable table1) {
        if (i >= 0) {
            DefaultTableModel model = (DefaultTableModel) table1.getModel();

            table1.changeSelection(i, 0, false, false);

            changRecordStatus(i, table1);

            if (i <= model.getRowCount()) {
                fillDataTableToForm(i);
            } else {
                lblDem.setText("No record");
            }
        }
    }

    public void changRecordStatus(int i, JTable table1) {
        DefaultTableModel model = (DefaultTableModel) table1.getModel();
        lblDem.setText(i + 1 + "/" + model.getRowCount());
    }
