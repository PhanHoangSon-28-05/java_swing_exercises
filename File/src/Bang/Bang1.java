    private void LoadDataJTable(ArrayList<NhanVien> myList) {
        Object[] columns = {"Mã SV", "Họ Tên SV", "Email", "Giới Tính", "Tình Trạng"};
        DefaultTableModel model = (DefaultTableModel) table1.getModel();
        model.setColumnIdentifiers(columns);
        model.setRowCount(0);
        if (myList.isEmpty() || myList == null) {
            model.setRowCount(0);
        } else {
            model.setRowCount(0);
            for (NhanVien nv : myList) {
                model.addRow(new Object[]{nv.getManv(), nv.getHoten(), nv.getEmail(), nv.getGioitinh(), nv.getTinhtrang()});
            
            }
            setSelectRow(currentSelected, table1);
        }
    }

