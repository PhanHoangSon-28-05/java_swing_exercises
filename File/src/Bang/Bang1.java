    private void bttOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttOpenActionPerformed
        JFileChooser mofile = new JFileChooser();
        int r = mofile.showOpenDialog(null);

        if (r == JFileChooser.APPROVE_OPTION) {

            try {
                String file = mofile.getSelectedFile().toString();
                FileInputStream fis = new FileInputStream(file);
                ObjectInputStream ois = new ObjectInputStream(fis);

                myList = (ArrayList<NhanVien>) ois.readObject();
                LoadDataJTable(myList);

            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Invalid format file");
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Class not found");
            }

        }
        // TODO add your handling code here:
    }//GEN-LAST:event_bttOpenActionPerformed

    private void bttSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttSaveActionPerformed
        System.err.println(myList);
        if (myList.equals("")) {
            JOptionPane.showMessageDialog(this, "Ban trong khong the xuat", "Erro", 0);
        } else {
            JFileChooser c = new JFileChooser();
            int r = c.showSaveDialog(null);
            if (r == JFileChooser.APPROVE_OPTION) {
                try {
                    String file = c.getSelectedFile().toString();

                    FileOutputStream fos = new FileOutputStream(file);
                    ObjectOutputStream oos = new ObjectOutputStream(fos);

                    oos.writeObject(myList);
                    JOptionPane.showMessageDialog(this, "File saved");
                } catch (IOException ex) {
                    System.out.println(ex.toString());
                }
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_bttSaveActionPerformed

    private void bttUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttUpdateActionPerformed
        int iSelect = table1.getSelectedRow();
        String maNV = txtMaNhanVien.getText();
        String hoten = txtHoTen.getText();
        String email = txtEmail.getText();
        String gioitinh = "";
        String tinhtrang = "";

        table1.setValueAt(maNV, iSelect, 0);
        table1.setValueAt(hoten, iSelect, 1);
        table1.setValueAt(email, iSelect, 2);
        if (rdoNam.isSelected()) {
            gioitinh = "Nam";
        } else {
            gioitinh = "Nữ";
        }
        table1.setValueAt(gioitinh, iSelect, 3);
        if (rdoDaKetHon.isSelected()) {
            tinhtrang = "Da kết hôn";
        } else {
            tinhtrang = "độc thân";
        }
        table1.setValueAt(tinhtrang, iSelect, 4);

        myList.set(iSelect, new NhanVien(maNV, hoten, email, gioitinh, tinhtrang));
        // TODO add your handling code here:
    }//GEN-LAST:event_bttUpdateActionPerformed
    public void fillDataTableToForm(int rows) {
        txtMaNhanVien.setText(table1.getValueAt(rows, 0).toString());
        txtHoTen.setText(table1.getValueAt(rows, 1).toString());
        txtEmail.setText(table1.getValueAt(rows, 2).toString());
        if (table1.getValueAt(rows, 3).toString() == "Nam") {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
        if (table1.getValueAt(rows, 4).toString() == "Đã kêt hôn") {
            rdoDaKetHon.setSelected(true);
        } else {
            rdoDocThan.setSelected(true);
        }
    }
