     private void initTable(){
        Object[] columns = {"Mã Độc Giả","Tên Độc Giả","Địa Chỉ","Email","Số Điện Thoại"};
        tblModel = new DefaultTableModel();
        tblModel.setColumnIdentifiers(columns);
        tblDocGia.setModel(tblModel);
    }
    private void LoadDataToTable(){
        try {
            DocGiaDao dao = new DocGiaDao();
            List<DocGia> list = dao.findAll();
            tblModel.setRowCount(0);
            for (DocGia docgia: list) {
                tblModel.addRow(new Object[]{docgia.getMaDG(),docgia.getTenDG(),docgia.getDiaChi(), docgia.getEmail(),
                docgia.getSDT()});
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }
