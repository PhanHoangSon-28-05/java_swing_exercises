    private void tblDocGiaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDocGiaMouseClicked
         try {
            int row = tblDocGia.getSelectedRow();
            
            if (row >= 0) {
                String id = (String) tblDocGia.getValueAt(row, 0);
                DocGiaDao dao = new DocGiaDao();
                DocGia docgia = dao.findByID(id);
                
                if (docgia != null) {
                    txtMaDocGia.setText(docgia.getMaDG());
                    txtTenDocGia.setText(docgia.getTenDG());
                    txtDiaChi.setText(docgia.getDiaChi());
                    txtEmail.setText(docgia.getEmail());
                    txtSDT.setText(docgia.getSDT());
//                    cbbTinhTrang.setText(sach.getTinhtrang());
                    
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.toString(), "Lỗi");
        }
// TODO add your handling code here:
    }//GEN-LAST:event_tblDocGiaMouseClicked

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        StringBuilder bd = new StringBuilder();
        DataValidator.validateEmpty(txtMaDOcGIa, bd, "Bạn Chưa Nhập Mã Độc Giả");
        
        if (bd.length() == 1) {
            MessageDialogHelper.showErrorDialog(parentForm, bd.toString(), "Lỗi");
        }
        
        try {
            DocGiaDao dao = new DocGiaDao();
            List<DocGia> list = dao.findByMaPhieu(txtMaDOcGIa.getText());
            tblModel.setRowCount(0);
            for (DocGia docgia: list) {
                tblModel.addRow(new Object[]{docgia.getMaDG(),docgia.getTenDG(),docgia.getDiaChi(), docgia.getEmail(),
                docgia.getSDT()});
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnTimKiemActionPerformed


