    private void Add_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Add_buttonActionPerformed
        student s = new student();
        s.setId(ID_TextField.getText());
        s.setName(Name_TextField.getText());
        try{
            s.setDate(new SimpleDateFormat("dd/MM/yyyy").parse(Birth_TextField.getText()));
        }catch(ParseException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Bạn nhập sai!","Eror", JOptionPane.ERROR_MESSAGE);
        }
        s.setAddress(Address_TextField.getText());
        s.setEmail(Email_TextField.getText());
        s.setPhone(Phone_TextField.getText());
        s.setMark(Float.parseFloat(Phone_TextField.getText()));
        list.add(s);
        // TODO add your handling code here:
        hinh_thuc_chay();
    }//GEN-LAST:event_Add_buttonActionPerformed

