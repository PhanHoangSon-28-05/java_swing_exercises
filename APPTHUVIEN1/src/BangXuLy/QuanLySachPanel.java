    private void tblSachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSachMouseClicked
         try {
            int row = tblSach.getSelectedRow();
            
            if (row >= 0) {
                String id = (String) tblSach.getValueAt(row, 0);
                SachDao dao = new SachDao();
                Sach sach = dao.findByID(id);
                
                if (sach != null) {
                    txtMaSach.setText(sach.getMasach());
                    txtTenSach.setText(sach.getTensach());
                    txtTheLoai.setText(sach.getTheloai());
                    txtTacGIa.setText(sach.getTacgia());
                    txtSoLuong.setText(String.valueOf(sach.getSoluong()));
//                    cbbTinhTrang.setText(sach.getTinhtrang());
                    
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.toString(), "Lỗi");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_tblSachMouseClicked


