    private void initTable(){
        Object[] columns = {"Mã Sách","Tên sách","Thể Loại","Tác Giả","Số Lượng","Tình Trạng"};
        tblModel = new DefaultTableModel();
        tblModel.setColumnIdentifiers(columns);
        tblSach.setModel(tblModel);
    }
    private void LoadDataToTable(){
        try {
            SachDao dao = new SachDao();
            List<Sach> list = dao.findAll();
            tblModel.setRowCount(0);
            for (Sach sach: list) {
                tblModel.addRow(new Object[]{sach.getMasach(),sach.getTensach(),sach.getTheloai(), sach.getTacgia(),
                sach.getSoluong(), sach.getTinhtrang()});
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }

