    private void tblStudentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStudentMouseClicked
        try {
            int row = tblStudent.getSelectedRow();
            
            if (row >= 0) {
                String id = (String) tblStudent.getValueAt(row, 0);
                SinhVienDao dao = new SinhVienDao();
                SinhVien sv = dao.findByID(id);
                
                if (sv != null) {
                    txtStudentID.setText(sv.getMaSinhVien());
                    txtName.setText(sv.getHoTen());
                    Date date = Date.valueOf(sv.getNgaySinh());
                    date_chooser.setDate(date);
                    txtEmail.setText(sv.getEmail());
                    txtPhone.setText(sv.getSoDT());
                    txaAddress.setText(sv.getDiaChi());
                    if (sv.getGioiTinh() == 1) {
                        rdoFemale.setSelected(true);
                    }else{
                        rdoMale.setSelected(true);
                    }
                    if (sv.getHinh() != null) {
                        Image img = ImageHelper.createImageFromByteArray(sv.getHinh(), "png");
                        lblImage.setIcon(new ImageIcon(img));
                        personalImage = sv.getHinh();
                    }else{
                        personalImage = sv.getHinh();
                        ImageIcon icon = new ImageIcon("S:/Java/BangDiem/AppSV/src/BangDangNhap/Icon/login1.png");
                        lblImage.setIcon(icon);
                    }
                    if (sv.getTinhTrang() == 1) {
                        ckbDangHoc.setSelected(true);
                    } else {
                        ckbNghiHoc.setSelected(true);
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.toString(), "Lỗi");
        }
// TODO add your handling code here:
    }//GEN-LAST:event_tblStudentMouseClicked

