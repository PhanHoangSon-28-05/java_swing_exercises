    private void LoadDataJTable(List<SinhVien1> myList) {
        
        DefaultTableModel model = (DefaultTableModel) tblStudent.getModel();

        model.setRowCount(0);
        if (myList.isEmpty() || myList == null) {
            model.setRowCount(0);
        } else {
            model.setRowCount(0);
            for (SinhVien1 sv : myList) {
                model.addRow(new Object[]{sv.getMaSinhVien(),sv.getHoTen(),sv.getNgaySinh(),sv.getEmail(),
                sv.getSoDT(), sv.getGioiTinh()== 0 ? "Nam":"Nữ", sv.getDiaChi(), sv.getTinhTrang()== 0 ? "Nghỉ Học":"Đang Học"});
            
            }
            
        }
    }
    
