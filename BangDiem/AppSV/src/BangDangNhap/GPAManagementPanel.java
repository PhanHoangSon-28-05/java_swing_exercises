    private void initTable(){
        Object[] columns = {"Mã Sinh Viên","JAVA","SQL","Phân Tích Hệ Thống","IOS","PHP", "Điểm Trung Bình"};
        tblModel = new DefaultTableModel();
        tblModel.setColumnIdentifiers(columns);
        tblBangDiem.setModel(tblModel);
    }
    private void LoadDataToTable(){
        try {
            BangDiemDao dao = new BangDiemDao();
            List<BangDiem> list = dao.findAll();
            tblModel.setRowCount(0);
            for (BangDiem bd : list) {
                tblModel.addRow(new Object[]{ bd.getMaSinhVien(), bd.getJava(), bd.getSQL(),
                                bd.getPhanTichHeThong(), bd.getIos(), bd.getPhp(),
                                (bd.getJava() + bd.getSQL() + bd.getPhanTichHeThong() + bd.getIos() + bd.getPhp())/5});
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parent, e.getMessage(), "Lỗi");
        }
    }

