    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtStudentID, sb, "Mã Sinh Viên Cần Phải Nhập");
        DataValidator.validateEmpty(txtIOS, sb, "IOS Cần phải nhập điểm");
        DataValidator.validateEmpty(txtJava, sb, "JAVA Cần phải nhập điểm");
        DataValidator.validateEmpty(txtName, sb, "Cần phải nhập tên");
        DataValidator.validateEmpty(txtPHP, sb, "Cần phải nhập điểm PHP");
        DataValidator.validateEmpty(txtPhanTichHeThong, sb, "Phân Tích Hệ Thống cần phải nhập điểm");
        DataValidator.validateEmpty(txtSql, sb, "SQL Cần phải nhập điểm");
        
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(parent, sb.toString(), "Lỗi");
            return;
        }
        
        try {
            
            BangDiem bd = new BangDiem();
            bd.setMaSinhVien(txtStudentID.getText());
            bd.setJava(Float.parseFloat(txtJava.getText()));
            bd.setSQL(Float.parseFloat(txtSql.getText()));
            bd.setPhanTichHeThong(Float.parseFloat(txtPhanTichHeThong.getText()));
            bd.setIos(Float.parseFloat(txtIOS.getText()));
            bd.setPhp(Float.parseFloat(txtPHP.getText()));
            
            BangDiemDao dao = new BangDiemDao();  
            if (dao.findByMaSinhVien(txtStudentID.getText()) != null) {
                if(MessageDialogHelper.showConfirmDialog(parent, "Trong Danh Sách Đã Mã Sinh Viên. \n Bạn Có Muốn Lưu Thêm?",
                        "Hỏi") == JOptionPane.NO_OPTION);
                return;
            }else{
                if (dao.insert(bd)) {
                    MessageDialogHelper.showMessageDialog(parent, "Bảng Điểm Đã Được Lưu", "Thông Báo");
                }else{
                    MessageDialogHelper.showErrorDialog(parent, "Bảng Điểm Không Lưu Được", "Thông Báo");
                }
            }
            LichSuDiem(txtStudentID.getText(), "Lưu");
            LoadDataToTable();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parent, e.toString(), "Lỗi");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnSaveActionPerformed

