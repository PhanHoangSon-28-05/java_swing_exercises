    private void btnTopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTopActionPerformed
        bangdiemhoi = new BangDiemHoi(parent, true);
        bangdiemhoi.setVisible(true);
        if (bangdiemhoi.LBLTop() == "") {
                return;
        }else{
            int top = Integer.parseInt(bangdiemhoi.LBLTop());

            try {
                BangDiemDao dao = new BangDiemDao();
                List<BangDiem> list = dao.findTop(top);
                tblModel.setRowCount(0);
                for (BangDiem bd : list) {
                    tblModel.addRow(new Object[]{ bd.getMaSinhVien(), bd.getJava(), bd.getSQL(),
                                    bd.getPhanTichHeThong(), bd.getIos(), bd.getPhp(),
                                    (bd.getJava() + bd.getSQL() + bd.getPhanTichHeThong() + bd.getIos() + bd.getPhp())/5});
               }
                tblModel.fireTableDataChanged();
            } catch (Exception e) {
                e.printStackTrace();
                MessageDialogHelper.showErrorDialog(parent, e.getMessage(), "Lỗi");
           }
        }
    }//GEN-LAST:event_btnTopActionPerformed

