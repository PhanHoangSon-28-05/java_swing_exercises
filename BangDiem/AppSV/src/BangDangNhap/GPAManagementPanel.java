    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtStudentID, sb, "Mã Sinh Viên Trống Hoặc Sai Mã Sinh Viên ");
        if (sb.length() >0) {
            MessageDialogHelper.showErrorDialog(parent, sb.toString(), "Lỗi");
        }
        
        if (MessageDialogHelper.showConfirmDialog(parent, "Bạn muốn cập nhập sinh viên không",
                "Hỏi") == JOptionPane.NO_OPTION ) {
            return;          
        }
        try {
            
            BangDiem bd = new BangDiem();
            bd.setMaSinhVien(txtStudentID.getText());
            bd.setJava(Float.parseFloat(txtJava.getText()));
            bd.setSQL(Float.parseFloat(txtSql.getText()));
            bd.setPhanTichHeThong(Float.parseFloat(txtPhanTichHeThong.getText()));
            bd.setIos(Float.parseFloat(txtIOS.getText()));
            bd.setPhp(Float.parseFloat(txtPHP.getText()));
            
            BangDiemDao dao = new BangDiemDao();
            if (dao.findByMaSinhVien(txtStudentID.getText()) != null) {
                if(MessageDialogHelper.showConfirmDialog(parent, "Bạn Có Muốn Cập Nhập Không?",
                        "Hỏi") == JOptionPane.NO_OPTION);
                return;
            }else{
                if (dao.update(bd)) {
                    MessageDialogHelper.showMessageDialog(parent, "Bảng Điểm Đã Được Lưu", "Thông Báo");
                }else{
                    MessageDialogHelper.showErrorDialog(parent, "Bảng Điểm Không Lưu Được", "Thông Báo");
                }
            }
            LichSuDiem(txtStudentID.getText(), "Cập nhập");
            LoadDataToTable();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parent, e.toString(), "Lỗi");
        }
        
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            if(txtStudentID.getText().equals("")){
                MessageDialogHelper.showMessageDialog(parent, "Mã Sinh Viên Cần Phải Nhâp", "Lỗi");
                return;
            }
            BangDiemDao dao = new BangDiemDao();
            BangDiem bd = dao.findByMaSinhVien(txtStudentID.getText());
            
            if (bd == null) {
                MessageDialogHelper.showMessageDialog(parent, "Mã Sinh Không Có Tồn Tại Trong Danh Sách", "Lỗi");
                return;
            } 
            if (MessageDialogHelper.showConfirmDialog(parent, "Bạn Có Muốn Xóa Điểm Của Sinh Viên Không"
                    , "Hỏi") == JOptionPane.NO_OPTION) {
                return;
            }
            if(dao.deleteByMaSinhVien(txtStudentID.getText())){
                MessageDialogHelper.showMessageDialog(parent, "Điểm Của Sinh VIên Đã Được Xóa", "Thông báo");
            }else{
                MessageDialogHelper.showMessageDialog(parent, "Điểm Của Sinh Viên Không Xóa Được", "Thông Báo");
            }
            LichSuDiem(txtStudentID.getText(), "Xóa");
            LoadDataToTable();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parent, e.toString(), "Lỗi");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteActionPerformed

