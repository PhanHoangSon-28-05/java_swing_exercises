    private void initTable(){
        Object[] columns = {"Mã Sinh Viên","Tên Lớp"};
        tblModel = new DefaultTableModel();
        tblModel.setColumnIdentifiers(columns);
        tblDanhSachLop.setModel(tblModel);
    }
    private void LoadDataToTable(){
        try {
            LopDao dao = new LopDao();
            List<Lop> list = dao.findAll();
            tblModel.setRowCount(0);
            for (Lop lop : list) {
                tblModel.addRow(new Object[]{ lop.getMaSinhVien(), lop.getTenLop()});
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parent, e.getMessage(), "Lỗi");
        }
    }
