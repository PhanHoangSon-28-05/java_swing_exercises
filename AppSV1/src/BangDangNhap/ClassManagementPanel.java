    private void LichSuClass(String MaSV, String GhiChu){   
        try {
            LichSu LS = new LichSu();
            LS.setMaSV(MaSV); 
            LS.setGhiChu(GhiChu);

            LichSuDao dao = new LichSuDao();
            dao.insert_Class(LS);

        } catch (Exception e) {
            e.printStackTrace();
            
        }
    }
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMaSinhVien, sb, "Chưa có nhập Mã Sinh Viên");
        DataValidator.validateEmpty(txtLop, sb, "Chưa nhập Tên Lớp");
        
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
        }
        
        try {
            Lop lop = new Lop();
            lop.setTenLop(txtLop.getText());
            lop.setMaSinhVien(txtMaSinhVien.getText());
            
            LopDao dao = new LopDao();
            
                if (dao.insert(lop)) {
                    MessageDialogHelper.showMessageDialog(parent, "Danh Sách Lớp Đã Được Lưu", "Thông Báo");
                }else{
                    MessageDialogHelper.showErrorDialog(parent, "Danh Sách Lớp Không Lưu Được", "Thông Báo");
                }
                LichSuClass(txtMaSinhVien.getText(), "Thêm Lớp");
             LoadDataToTable();
        } catch (Exception e) {
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            if(txtMaSinhVien.getText().equals("")){
                MessageDialogHelper.showErrorDialog(parent, "Mã Sinh Viên Cần Phải Nhâp", "Lỗi");
                return;
            }
            LopDao dao = new LopDao();
            Lop lop = dao.findByMaSinhVien(txtMaSinhVien.getText());
            
            if (lop == null) {
                MessageDialogHelper.showMessageDialog(parent, "Mã Sinh Viên Không Có Tồn Tại Trong Danh Sách", "Lỗi");
                return;
            } 
            if (MessageDialogHelper.showConfirmDialog(parent, "Bạn Có Muốn Xóa Điểm Của Sinh Viên Không"
                    , "Hỏi") == JOptionPane.NO_OPTION) {
                return;
            }
            if(dao.delete(txtMaSinhVien.getText())){
                MessageDialogHelper.showMessageDialog(parent, "Điểm Của Sinh VIên Đã Được Xóa", "Thông báo");
            }else{
                MessageDialogHelper.showMessageDialog(parent, "Điểm Của Sinh Viên Không Xóa Được", "Thông Báo");
            }
            LichSuClass(txtMaSinhVien.getText(), "Xóa");
            LoadDataToTable();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parent, e.toString(), "Lỗi");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteActionPerformed

